class Car_Data:

    def __init__(self, color, owners_count, year, price,
                 region, mileage, doors_count, class_auto,
                 name_auto, trunk_volume, brand, model):
        self.color = color
        self.owners_count = owners_count
        self.year = year
        self.price = price
        self.region = region
        self.mileage = mileage
        self.doors_count = doors_count
        self.class_auto = class_auto
        self.name_auto = name_auto
        self.trunk_volume = trunk_volume
        self.brand = brand
        self.model = model
